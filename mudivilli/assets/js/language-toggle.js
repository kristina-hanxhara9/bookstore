// Set default language from localStorage or default to English
let currentLanguage = localStorage.getItem('language') || 'en';

// Function to switch language
function switchLanguage() {
  // Toggle between languages
  currentLanguage = currentLanguage === 'en' ? 'ta' : 'en';
  
  // Save preference to localStorage
  localStorage.setItem('language', currentLanguage);
  
  // Update UI elements
  updateUILanguage();
}

// Function to update all UI elements with the selected language
function updateUILanguage() {
  // Get all elements with data-i18n attribute
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[currentLanguage][key]) {
      // Special handling for placeholders
      if (element.hasAttribute('placeholder')) {
        element.setAttribute('placeholder', translations[currentLanguage][key]);
      } 
      // Special handling for buttons with icons
      else if (element.classList.contains('cart-btn') && key === 'add_to_cart') {
        const icon = '<i class="fas fa-shopping-cart"></i> ';
        element.innerHTML = icon + translations[currentLanguage][key];
      }
      // Special handling for search button
      else if (element.classList.contains('search_button') && key === 'search_button') {
        const icon = ' <i class="fas fa-search"></i>';
        element.innerHTML = translations[currentLanguage][key] + icon;
      }
      // Special handling for team bios (HTML content)
      else if (element.classList.contains('team-bio')) {
        element.innerHTML = translations[currentLanguage][key];
      }
      // Regular text content update
      else {
        element.textContent = translations[currentLanguage][key];
      }
    }
  });
  
  // Update toggle buttons text
  updateToggleButtonsText();
  
  // Add language class to body
  document.body.className = currentLanguage === 'ta' ? 'tamil' : 'english';
  
  // Update HTML lang attribute
  document.documentElement.lang = currentLanguage;
}

// Function to update toggle button text when language changes
function updateToggleButtonsText() {
  const toggleButtons = document.querySelectorAll('.language-btn');
  const newText = currentLanguage === 'en' ? 'தமிழ்' : 'English';
  
  toggleButtons.forEach(button => {
    button.textContent = newText;
  });
}

