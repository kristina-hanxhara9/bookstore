// Function to initialize language toggle buttons
function initLanguageToggle() {
  // Get all possible header containers to try
  const desktopParents = [
    '.header-icons', 
    '.header-meta-area', 
    '.header-area', 
    '.top-header-area',
    'header',
    '.site-header'
  ];
  
  const mobileParents = [
    '.mean-bar',
    '.mobile-menu',
    '.main-menu-wrap',
    '.responsive-menu',
    '.mobile-nav'
  ];
  
  // Try each possible container for desktop
  let desktopSuccess = false;
  for (const selector of desktopParents) {
    if (createToggleButton(selector, 'language-toggle', 'desktop-toggle-container')) {
      console.log(`Successfully created desktop toggle in ${selector}`);
      desktopSuccess = true;
      break;
    }
  }
  
  // Try each possible container for mobile
  let mobileSuccess = false;
  for (const selector of mobileParents) {
    if (createToggleButton(selector, 'mobile-language-toggle', 'mobile-toggle-container')) {
      console.log(`Successfully created mobile toggle in ${selector}`);
      mobileSuccess = true;
      break;
    }
  }
  
  // If all else fails, create a fixed position toggle that's always visible
  if (!desktopSuccess && !mobileSuccess) {
    console.log("Could not find header elements, creating fixed toggle button");
    createFixedToggleButton();
  }
}

// Function to create a fixed toggle button that's always visible
function createFixedToggleButton() {
  if (!document.getElementById('fixed-language-toggle')) {
    const container = document.createElement('div');
    container.id = 'fixed-toggle-container';
    container.className = 'fixed-toggle-container';
    
    const button = document.createElement('button');
    button.id = 'fixed-language-toggle';
    button.className = 'language-btn';
    button.textContent = currentLanguage === 'en' ? 'தமிழ்' : 'English';
    
    button.addEventListener('click', function() {
      switchLanguage();
    });
    
    container.appendChild(button);
    document.body.appendChild(container);
    
    return true;
  }
  return false;
}

// Function to create toggle buttons
function createToggleButton(parentSelector, buttonId, containerId) {
  // First check if button already exists
  if (document.getElementById(buttonId)) {
    return true; // Button already exists
  }
  
  // Try to find the parent element
  const parent = document.querySelector(parentSelector);
  if (!parent) {
    console.log(`Parent element not found: ${parentSelector}`);
    return false;
  }
  
  // Create container for better positioning
  const container = document.createElement('div');
  container.id = containerId;
  container.className = 'toggle-container';
  
  // Create the button
  const button = document.createElement('button');
  button.id = buttonId;
  button.className = 'language-btn';
  button.textContent = currentLanguage === 'en' ? 'தமிழ்' : 'English';
  
  // Add click event listener
  button.addEventListener('click', function() {
    switchLanguage();
    console.log('Language switched to: ' + currentLanguage);
  });
  
  // Append button to container
  container.appendChild(button);
  
  // For desktop header, try to add to the header-icons first, then fallback to parent
  if (buttonId === 'language-toggle') {
    if (parentSelector === '.header-icons') {
      parent.appendChild(container);
    } else {
      const headerRight = parent.querySelector('.header-icons');
      if (headerRight) {
        headerRight.appendChild(container);
      } else {
        parent.appendChild(container);
      }
    }
  } 
  // For mobile menu, add directly to parent
  else {
    parent.appendChild(container);
  }
  
  return true;
}

// Function to handle scroll behavior for floating toggle
window.addEventListener('scroll', function() {
  // Try to find header element with fallbacks
  const headerElement = document.querySelector('.header-area') || 
                         document.querySelector('.top-header-area') || 
                         document.querySelector('header') || 
                         document.querySelector('.site-header');
  
  // Check if header is visible (with null/undefined protection)
  const headerVisible = headerElement ? isElementInViewport(headerElement) : false;
  
  // Check if mobile menu is visible
  const mobileElement = document.querySelector('.mean-bar') || 
                         document.querySelector('.mobile-menu');
  const mobileMenuVisible = mobileElement ? 
    (window.innerWidth <= 991 && mobileElement.style.display !== 'none') : 
    false;
  
  // Get or create floating toggle
  let floatingToggle = document.querySelector('.floating-toggle');
  
  // Create floating toggle if it doesn't exist
  if (!floatingToggle) {
    const floating = document.createElement('div');
    floating.className = 'floating-toggle';
    
    const button = document.createElement('button');
    button.className = 'language-btn';
    button.textContent = currentLanguage === 'en' ? 'தமிழ்' : 'English';
    button.addEventListener('click', switchLanguage);
    
    floating.appendChild(button);
    document.body.appendChild(floating);
    
    floatingToggle = floating;
  }
  
  // Show floating toggle only when other toggles are not visible
  if (!headerVisible && !mobileMenuVisible) {
    floatingToggle.style.display = 'block';
  } else {
    floatingToggle.style.display = 'none';
  }
});

// Helper function to check if an element is in the viewport (with null protection)
function isElementInViewport(el) {
  if (!el) return false;
  
  const rect = el.getBoundingClientRect();
  return (
    rect.top >= 0 &&
    rect.left >= 0 &&
    rect.bottom <= (window.innerHeight || document.documentElement.clientHeight) &&
    rect.right <= (window.innerWidth || document.documentElement.clientWidth)
  );
}

// Handle window resize events to ensure toggle visibility
window.addEventListener('resize', function() {
  const desktopToggle = document.getElementById('desktop-toggle-container');
  const mobileToggle = document.getElementById('mobile-toggle-container');
  
  if (window.innerWidth <= 991) {
    if (desktopToggle) desktopToggle.style.display = 'none';
    if (mobileToggle) mobileToggle.style.display = 'block';
  } else {
    if (desktopToggle) desktopToggle.style.display = 'inline-block';
    if (mobileToggle) mobileToggle.style.display = 'none';
  }
});